# Python Operators
# Operators are used to perform operations on variables and values.
# e.g 
# + operator use for concat string and also use for addinf two value
# print("Hello" + "World")
# print(10+5)

# Python divides the operators in the following groups:

# 1. Arithmetic operators (+, -, *, /, %, **, //)

# e.g

value_of_a = 77
value_of_b = 63

# addition
print("Sum: ", value_of_a + value_of_b )

# Subtraction
print("Subtraction", value_of_a - value_of_b)

# multiplication
print("Multiplication: ", value_of_a * value_of_b)

# division
print("Division", value_of_a/value_of_b)

# floor division
print("Floor division: ", value_of_a // value_of_b)

# Modulo
print("Modulo: " ,value_of_a % value_of_b)

# a to the power b
print("Power: " , value_of_a ** value_of_b)

# 2. Assignment operators (=, +=, -=, *= , /= , %=, **=, //=, &=, !=, ^=, >>=, <<=)

# e.g
# assign 10 to var a
var_a = 10

# assign 5 to var b
var_b = 5

# assign the sum of a and b to a

# var_a = var_a + var_b

var_a += var_b

print(var_a)

# 3. Comparison operators(==, !=, >, <, >=, <=)
# e.g

a = 5

b = 2

# equal to operator
print('a == b =', a == b)

# not equal to operator
print('a != b =', a != b)

# greater than operator
print('a > b =', a > b)

# less than operator
print('a < b =', a < b)

# greater than or equal to operator
print('a >= b =', a >= b)

# less than or equal to operator
print('a <= b =', a <= b)


